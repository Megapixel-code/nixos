local function drawing_stuff( drawing )
   return {
      type = "text",
      oldfiles_directory = false,
      align = "center",
      fold_section = false,
      title = "Header",
      margin = 0,
      highlight = "Statement",
      default_color = "",
      oldfiles_amount = 0,

      content = drawing,
   }
end

local function random_drawing()
   math.randomseed( os.time() )
   local selected_drawing = drawings[math.random( #drawings )]
   return drawing_stuff( selected_drawing )
end


local function content()
   return {
      type = "mapping",
      oldfiles_directory = false,
      align = "center",
      fold_section = false,
      title = "Basic Commands",
      margin = 0,
      content = {
         { "ΎΖε Search File", "Telescope find_files", "<leader>sf" },
         { "≤░ΞΚ Search Grep", "lua require('config.telescope.multigrep').multigrep()", "<leader>sg" },
         { "≤░ΚΜ Yazi Change CWD", "lua require('yazi').yazi({change_neovim_cwd_on_close = true})", "<leader>yc" },
         { "ΌΙτ Search Themes", "ColorSkimerToggle", "<leader>sc" },
         { "≤░ΜΨ Search Help", "Telescope help_tags", "<leader>sh" },
      },
      highlight = "String",
      default_color = "",
      oldfiles_amount = 0,
   }
end


return {
   "startup-nvim/startup.nvim",
   dependencies = {
      "nvim-telescope/telescope.nvim",
